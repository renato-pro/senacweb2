// 3) Qual o turno que você estuda?

let turno = (prompt('Digite o turno que você estuda: (M) Manhã, (T) Tarde ou Noite (N)'))

if (turno == 'M' || turno =='m') {
    alert (`Bom dia! Você estuda de Manhã (${turno}).`)
    }
    else if (turno == 'T' || turno =='t') {
        alert (`Boa tarde! Você estuda de Tarde (${turno}).`)
    }
    else if (turno == 'N' || turno =='n') {
        alert (`Boa Noite! Você estuda de Noite (${turno}).`)
    }
    else {
        alert (`Você digitou o valor errado, favor verificar!`)
    }




   


