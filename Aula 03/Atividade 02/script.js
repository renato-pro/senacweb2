// 2) O número é positivo, negativo ou zero?

let numero = Number (prompt('Digite um número:'))

if (numero > 0) {
    alert (`O número ${numero} é maior que zero.`)
    }
    else if (numero < 0) {
        alert (`O número ${numero} é menor que zero.`);
    }
    else{
        alert (`O número ${numero} é igual zero.`);
    }

