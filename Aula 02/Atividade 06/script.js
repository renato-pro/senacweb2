// *** Aula 02 - Atividade 06 - Elaborar um Algoritmo para calcular a área de um Retângulo ***

let base = Number (prompt('Digite o valor da base (m):'));
let altura = Number (prompt('Digite o valor da altura (m):'));
let area = Number(base * altura);
alert (`O valor da área do retângulo de base ${base} m e altura ${altura} m é igual a ${area} m².`)