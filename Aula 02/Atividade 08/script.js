// *** Aula 02 - Atividade 08 - Elaborar um Algoritmo para calcular a área de um Triângulo ***

let base = Number (prompt('Digite o valor da base (m):'));
let altura = Number (prompt('Digite o valor da altura (m):'));
let area = Number(base * altura / 2);
alert (`O valor da área do Triângulo de base ${base} m e altura ${altura} m é igual a ${area} m².`);