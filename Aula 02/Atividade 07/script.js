// *** Aula 02 - Atividade 07 - Elaborar um Algoritmo para calcular a área de um Losango ***

let diagonalMaior = Number (prompt('Digite o valor da Diagonal Maior (m):'));
let diagonalMenor = Number (prompt('Digite o valor da Diagonal Menor (m):'));
let area = Number((diagonalMaior * diagonalMenor) / 2);
alert (`O valor da área de um Losango com a Diagonal Maior de ${diagonalMaior} m e com a Diagonal Menor de ${diagonalMenor} m é igual a ${area} m².`);


