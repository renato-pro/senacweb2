// *** Atividades 01 a 04 - Realizar operações Matemáticas através do prompt ***

let num1 = Number (prompt('Digite o primeiro número:'));
let num2 = Number (prompt('Digite o segundo número:'));
let res_soma = (num1 + num2);
alert ('O resultado da soma ' + num1 + ' + ' + num2 + ' = ' + res_soma);
let res_sub = (num1 - num2);
alert ('O resultado da subtração ' + num1 + ' - ' + num2 + ' = ' + res_sub);
let res_mult = (num1 * num2);
alert ('O resultado da multiplicação ' + num1 + ' * ' + num2 + ' = ' + res_mult);
let res_div = (num1 / num2);
alert ('O resultado da divisão ' + num1 + ' / ' + num2 + ' = ' + res_div);